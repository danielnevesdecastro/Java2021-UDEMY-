class OperacoesAritimeticas {

	public static void main ( String[] args){
		int soma = 2 + 10;
		int subtracao = 6 - 10;
		int multiplicacao = 8 * 3;
		int divisao = 8 / 2;
		int moduloResto = 7 % 2; 
		System.out.println ("SOMA :"+soma);
		System.out.println ("SUBTRACÃO :"+subtracao);
		System.out.println ("MULTIPLICAÇÃO :"+multiplicacao);
		System.out.println ("DIVISÃO: "+divisao);
		System.out.println ("RESTO DA DIVISÃO :"+moduloResto);
		int notaAluno1 = 99;
		int notaAluno2 = 80;
		int notaAluno3 = 53;
		int totalGeral = notaAluno1 + notaAluno2 + notaAluno3;
		System.out.println ("NOTAS : " + notaAluno1  + notaAluno2 + notaAluno3);
		System.out.println ("SOMA DAS NOTAS : " + totalGeral);
	}


}
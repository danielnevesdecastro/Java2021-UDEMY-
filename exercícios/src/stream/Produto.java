package stream;

//204 - Desafio Filter 
public class Produto {
	String nome;
	double preco;
	double desconto;
	double valorFrete;

	public Produto(String nome, double preco, double desconto, double valorFrete) {
		super();
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.valorFrete = valorFrete;
	}

}

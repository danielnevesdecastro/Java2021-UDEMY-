package api;

//aula 536 - Api, Math
public class ExplorandoMath {
	public static void main(String[] args) {
		
		System.out.println(Math.PI);//PI
		System.out.println(Math.pow(2, 3));// exponenciação
		System.out.println(Math.abs(-10.5));//absoluto, retorna como positivo
		System.out.println(Math.min(70,7));// retorna menor valor 
		System.out.println(Math.max(55,2));//retorna mairo valor
		System.out.println(Math.ceil(6.58));//arredonda pra cima
		System.out.println(Math.floor(6.58));//arredonda pra baixo
		System.out.println(Math.random());//gera valor random
		//System.out.println(Math.);//
		//System.out.println(Math.);//
	}
}

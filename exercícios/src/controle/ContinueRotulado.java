package controle;
//aula 84
public class ContinueRotulado {
	public static void main(String[] args) {
		// evite o uso !!
		externo: for (int i = 0; i < 3; i++) { // externo = nome do laço 
			 for (int j = 0; j < 3; j++) {

				if (i == 1) {
					continue externo;
				}
				System.out.printf("[%d %d]", i, j);
			}
			System.out.println();
		}
		System.out.println("fim");
	}
}
